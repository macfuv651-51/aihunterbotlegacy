"""
neuro/config.py
---------------
Центральный конфигурационный файл нейросетевого модуля.

Все гиперпараметры, пути к файлам и архитектурные настройки
хранятся здесь. Изменять только этот файл при необходимости
настроить модель под другой каталог или другие условия.
"""

import os


# ─── Пути ─────────────────────────────────────────────────────────────────────

BASE_DIR = os.path.dirname(os.path.abspath(__file__))

# Директория для сохранения весов модели после обучения
WEIGHTS_DIR = os.path.join(BASE_DIR, "weights")

# Директория для FAISS индекса (создаётся после build_index)
INDEX_DIR = os.path.join(BASE_DIR, "index")

# Путь к каталогу товаров (products.json из hunter)
PRODUCTS_FILE = os.path.join(BASE_DIR, "..", "hunter", "data", "products.json")

# Директория для обучающих данных (реальные сообщения из чатов)
DATA_DIR = os.path.join(BASE_DIR, "data")

# Файл с реальными сообщениями (одна строка = одно сообщение)
REAL_MESSAGES_FILE = os.path.join(DATA_DIR, "real_messages.txt")

# Файл с размеченными парами (сообщение \t правильный_товар)
LABELED_FILE = os.path.join(DATA_DIR, "labeled.tsv")


# ─── Архитектура модели ───────────────────────────────────────────────────────

# Размер символьного эмбеддинга
EMBED_DIM = 64

# Количество голов внимания
NUM_HEADS = 4

# Размер скрытого слоя в Feed-Forward Network
FF_DIM = 256

# Количество слоёв Transformer Encoder
NUM_LAYERS = 2

# Размерность выходного вектора
OUTPUT_DIM = 128

# Максимальная длина входной последовательности (символов)
# Названия товаров обычно 5-25 символов — 32 достаточно
MAX_SEQ_LEN = 32

# Dropout для регуляризации (предотвращает переобучение)
DROPOUT_RATE = 0.1


# ─── Обучение ─────────────────────────────────────────────────────────────────

# Начальный learning rate (скорость обучения)
LEARNING_RATE = 3e-4

# Минимальный learning rate (конечная точка cosine decay)
MIN_LEARNING_RATE = 1e-5

# Размер батча
BATCH_SIZE = 256

# Количество эпох
EPOCHS = 50

# Margin для Triplet Loss (минимальный запас между positive и negative)
TRIPLET_MARGIN = 0.3

# Количество синтетических вариантов на каждый товар при аугментации
# 53 товара × 200 = 10,600 вариантов
AUGMENT_PER_PRODUCT = 500

# Количество троек (anchor, positive, negative) на каждый товар
# 53 товара × 150 = 7,950 троек для обучения
TRIPLETS_PER_PRODUCT = 500


# ─── Инференс ─────────────────────────────────────────────────────────────────

# Количество ближайших соседей при поиске в FAISS
TOP_K = 5

# Порог уверенности для автоматической отправки (без участия админа)
AUTO_SEND_THRESHOLD = 0.92

# Порог для отправки на ручной review админу
REVIEW_THRESHOLD = 0.75


# ─── Ограничения ресурсов (для слабых машин / 4 ядра / 8 ГБ) ─────────────────

# Максимальное число потоков TensorFlow (0 = авто, лучше ограничить)
TF_INTRA_THREADS = 2   # потоки внутри одной операции
TF_INTER_THREADS = 2   # потоки между операциями

# Лимит RAM для TensorFlow в байтах (4 ГБ — половина от 8 ГБ)
# None = без лимита (не рекомендуется на 8 ГБ ОЗУ)
TF_MEMORY_LIMIT_MB = 3072  # 3 ГБ

# Уменьшенный batch size для обучения на слабой машине
# Если лагает при обучении — уменьши до 64 или 32
BATCH_SIZE_WEAK = 64
